//Igual (==)
//Retorna verdadeiro caso os operandos sjam iguais. 3 ==var1
"3" == var1
3 == '3'

//Não igual (!=)
//Retorna verdadeiro caso os operandos não sejam iguais. var1 != 4
var2 != "3"

//Estritamente igual (===)
//Retorna verdadeiro caso os operandos sejam iguais e do mesm tipo. Veja também Objects.is e igualdade
3 === var1

//Estrtamente não iguaal (!==)
//Retorna verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo.
var1 !== "3"
3 !== '3'

//Maior que (>)
//Retorna verdadeiro caso o operador da esquerda seja maior que o da direita.
var2 > var1;
"12" > 2

//Maior que ou igual (>=)
//Retorna verdadeiro caso o operador da esquerda seja maior que ou igual o da direita.
var2 >= var1
var1 >= 3

//Menor que (<)
//Retorna verdadeiro caso o operador da esquerda seja menor que o da direita.
var2 < var1;
"12" < 2

//Menor que ou igual (<=)
//Retorna verdadeiro caso o operador da esquerda seja menor que ou igual o da direita.
var2 <= var1
var1 <= 3